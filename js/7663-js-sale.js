const mint1free= async (e)=>{
	if (typeof window.ethereum !== 'undefined') {
		// amount of free mint
		  const _mintAmount = 1
		  if (_mintAmount > 0){
			var result="";
			try{
			  const accounts=await window.ethereum.request({ method: 'eth_requestAccounts' })
			  const web3= new Web3(window.ethereum);
			  const contract=new web3.eth.Contract(abi, CONTRACT_ADDR, {gas: 3000000});
			  const paused = await contract.methods.paused().call()
			  if (paused) throw {"message":"Sale is not active"}
			  const price = await contract.methods.cost().call()
			  const value = (0 * _mintAmount)
			  const gas = Math.round( await contract.methods.mint(_mintAmount).estimateGas({value: value.toString(), from: accounts[0]}) * 1.2 )
			  result = await contract.methods.mint(_mintAmount).send({value: value.toString(), from: accounts[0], gas: gas})
			}catch(e){
			  alert("Error: " + e.message)
			  console.log("Error: ",e)
			}
		  }
	}     
  }
  
